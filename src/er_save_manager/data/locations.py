def get_location_by_key(key: str) -> Location | None:
    """Get a location by its key."""
    return ALL_LOCATIONS.get(key)


def get_map_name(map_id: MapId) -> str:
    """Get human-readable map name from MapId."""
    map_str = f"m{map_id.data[3]:02d}_{map_id.data[2]:02d}_{map_id.data[1]:02d}_{map_id.data[0]:02d}"
    return MAP_NAMES.get(map_str, map_str)


def get_all_locations() -> list[Location]:
    """Get all known teleport locations."""
    return list(ALL_LOCATIONS.values())


def get_all_locations_sorted() -> list[tuple[str, Location]]:
    """Get all locations sorted by region and name, with duplicates removed."""
    # Use dict to track unique locations by (region, grace_name/name)
    seen = {}
    for key, loc in ALL_LOCATIONS.items():
        unique_key = (loc.region, loc.grace_name or loc.name)
        if unique_key not in seen:
            seen[unique_key] = (key, loc)

    # Sort by region, then name
    return sorted(seen.values(), key=lambda x: x[1].sort_key)


