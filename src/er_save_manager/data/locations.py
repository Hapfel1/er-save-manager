@dataclass
class Location:
    """A location in Elden Ring."""

    map_id: MapId
    name: str
    region: str
    coordinates: FloatVector3 | None = None
    is_safe: bool = True
    is_dlc: bool = False
    grace_name: str = ""

    @property
    def region_id(self) -> int:
        """Get region ID for this location's region."""
        return get_region_id(self.region)

    @property
    def display_name(self) -> str:
        """Get display name for UI: Region - Grace Name - map_id"""
        map_str = f"m{self.map_id.data[3]:02d}_{self.map_id.data[2]:02d}_{self.map_id.data[1]:02d}_{self.map_id.data[0]:02d}"
        if self.grace_name:
            return f"{self.region} - {self.grace_name} - {map_str}"
        return f"{self.region} - {self.name} - {map_str}"

    @property
    def short_display_name(self) -> str:
        """Get short display name without map ID."""
        if self.grace_name:
            return f"{self.region} - {self.grace_name}"
        return f"{self.region} - {self.name}"

    @property
    def sort_key(self) -> tuple:
        """Get sort key for consistent ordering by region, then name."""
        return (self.region, self.grace_name or self.name)


